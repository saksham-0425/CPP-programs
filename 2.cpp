#include<iostream>
using namespace std;
int main(){
    int num1,num2;         //<< this is called an insertion operator
    cout<<"enter the value of num1:\n";
    cin>>num1;                          //>>this is called as an extraction operator
    cout<<"enter the value of num2:\n";
    cin>>num2;                          //>>this is called as an extraction operator
    cout<<"the sum is "<<num1+num2;
}